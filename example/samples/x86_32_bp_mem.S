main:
	PUSH EBP
	MOV  EBP, ESP
	SUB  ESP, 0x10
	CALL getpc
getpc:
	POP ESI
	LEA EAX, DWORD PTR [ESI + my_data - _ + 0x4]
	MOV ECX, 0x0
loop:
	MOV EDX, DWORD PTR [EAX]
	MOV ESI, DWORD PTR [ESP]
	MOV DWORD PTR [ESP], ESI
	INC ECX
	INC EAX
	CMP ECX, 0x10
	JNZ loop
	MOV ESP, EBP
	POP EBP
	RET


.align 0x100
my_data:
.dword	0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0
.dword	0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0
.dword	0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0
